// --- SECURE STANDALONE ADMINISTRATIVE LOGIN MODULE ---

// Chef module removed completely. Context is permanently locked to manager dashboard authorization flow.
const currentType = 'manager';

// Handle Form Submission
const loginForm = document.getElementById('loginForm');
if (loginForm) {
    loginForm.onsubmit = function(e) {
        e.preventDefault();
        
        // Element Extraction Nodes
        const userField = document.getElementById('username');
        const passField = document.getElementById('password');
        const errorMsg = document.getElementById('errMsg');
        const submitBtn = document.getElementById('submitBtn') || this.querySelector('button[type="submit"]');

        if (!userField || !passField || !errorMsg) {
            console.error("Critical Failure: Authentication DOM elements are unmapped.");
            return;
        }

        const user = userField.value.trim();
        const pass = passField.value;

        // Clear previous runtime error warnings
        errorMsg.innerText = "";
        
        // UI Interaction Freeze State Change (Prevents multi-click processing errors)
        let originalText = "ENTER SYSTEM";
        if (submitBtn) {
            originalText = submitBtn.innerText;
            submitBtn.innerText = "VERIFYING...";
            submitBtn.disabled = true;
        }

        // API call to authentication endpoint
        fetch('/api/login', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ 
                username: user, 
                password: pass, 
                loginType: currentType 
            })
        })
        .then(res => {
            if (!res.ok) {
                throw new Error(`Authentication server returned invalid status frame: ${res.status}`);
            }
            return res.json();
        })
        .then(data => {
            if (data.success) {
                // Secure redirection targeting the server routing matrix payload or dynamic dashboard baseline
                window.location.href = data.redirect || 'manager.html';
            } else {
                // Display specific security validation failures generated by database logic
                errorMsg.innerText = data.message || "Invalid administrative credentials supplied.";
                
                // Restore interactive state controls on input validation failure
                if (submitBtn) {
                    submitBtn.innerText = originalText;
                    submitBtn.disabled = false;
                }
            }
        })
        .catch(err => {
            console.error("Login Module Transmission Error:", err);
            errorMsg.innerText = "System Interface Offline. Please ensure backend services are functional.";
            
            // Restore interactive state controls on core pipeline crash
            if (submitBtn) {
                submitBtn.innerText = originalText;
                submitBtn.disabled = false;
            }
        });
    };
}